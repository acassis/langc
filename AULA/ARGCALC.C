#include <stdio.h>
#include <conio.h>
#include <stdlib.h>
#include <string.h>
void main(int argc,char *argv[])
{
  float  a,b,f;
  clrscr();
  if(argc!=4)
     {puts("parametros invalidos");
     exit(1);
     }
     a=atof(argv[1]);
     b=atof(argv[3]);
switch(*argv[2]){
case '+':
printf("\n %f + %f = %f",a,b,a+b);
break;
case '-':
printf("\n %f - %f = %f",a,b,a-b);
break;
case '/':
if(b==0)
{
  puts("numero nao pode ser dividido por zero");
  exit(1);
}
f=a/b;
printf("\n %f / %f = %f",a,b,f );
break;
case '*':
printf("\n %f * %f = %f",a,b,a*b);
break;
}
}
